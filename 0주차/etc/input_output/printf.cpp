#include <bits/stdc++.h>

using namespace std;

int a = 1, b = 2;
char c = 'a';
string str = "네버랜드";
double d = 1.2342323;
long long e = 1000000000L;

int main(void){
    printf("a : %d, b : %d\n", a, b);
    printf("c : %c\n", c);
    printf("str : %s\n", str.c_str());
    printf("d : %lf\n", d);
    printf("e : %lld\n", e);
}